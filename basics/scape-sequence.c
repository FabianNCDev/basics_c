// escape sequence

#include <stdio.h>

void main() {
  // nueva linea
  printf("nueva linea\n");
  // texto tabulado
  printf("\ttexto con tab\n");
  // texto con retroceso de un caracter
  printf("texto con \bbackspace\n");
  // doble comillas
  printf("esta es mi doble comilla -> \"\n");
  // slash en texto
  printf("este es mi slash \\\n");
}
